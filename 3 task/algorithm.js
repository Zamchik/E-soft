function isValid(s) {
    // Проверка на нечётную длины строки
    if (s.length % 2 !== 0) return false;
    
    const stack = [];
    // Соответствие закрывающей скобки открывающей
    const pairs = {
        ')': '(',
        ']': '[',
        '}': '{'
    };
    
    for (let char of s) {
        // Если char - закрывающая скобка
        if (pairs[char]) {
            // Извлекаем верхний элемент стека или используем заглушку, если стек пуст
            const top = stack.pop();
            // Если верхний элемент не соответствует текущей закрывающей скобке
            if (top !== pairs[char]) {
                return false;
            }
        } else {
            // Иначе это открывающая скобка - кладём в стек
            stack.push(char);
        }
    }
    
    // После обработки всех символов стек должен быть пуст
    return stack.length === 0;
}